import numpy as np

# Datos iniciales
presion = np.array([0.5, 1.0, 1.5, 2.0, 2.5, 3.0])
volumen = np.array([1.62, 1.0, 0.75, 0.62, 0.52, 0.46])

# Ajustar un polinomio de grado 3 (cúbico)
coef_cubico = np.polyfit(presion, volumen, 3)
pol_cubico = np.poly1d(coef_cubico)

# Evaluar el polinomio cúbico para presión de 3.5
volumen_3_5_cubico = pol_cubico(3.5)
print(f"El volumen estimado para una presión de 3.5 usando un polinomio cúbico es: {volumen_3_5_cubico:.4f}")
